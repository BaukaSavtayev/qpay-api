<?php

namespace App\Http\Requests\Business\Transaction;

class GetTransactionRequest extends \Illuminate\Foundation\Http\FormRequest
{
    /**
     * Determine if the user is authorized to make this request.
     *
     * @return bool
     */
    public function authorize()
    {
        return auth()->check();
    }

    /**
     * Get the validation rules that apply to the request.
     *
     * @return array<string, mixed>
     */
    public function rules()
    {
        return [
            'time_interval' => 'numeric|exists:App\Models\City,id'
        ];
    }
}
